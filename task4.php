<!--Объявить массив, индексами которого являются буквы русского языка,
а значениями – соответствующие латинские буквосочетания
(‘а’=> ’a’, ‘б’ => ‘b’, ‘в’ => ‘v’, ‘г’ => ‘g’, …, ‘э’ => ‘e’,
‘ю’ => ‘yu’, ‘я’ => ‘ya’).
Написать функцию транслитерации строк.-->


<?php
// Массив соответствий русских букв латинским буквосочетаниям
$alfabet = [
    'а' => 'a',   'б' => 'b',   'в' => 'v',
    'г' => 'g',   'д' => 'd',   'е' => 'e',
    'ё' => 'e',   'ж' => 'zh',  'з' => 'z',
    'и' => 'i',   'й' => 'y',   'к' => 'k',
    'л' => 'l',   'м' => 'm',   'н' => 'n',
    'о' => 'o',   'п' => 'p',   'р' => 'r',
    'с' => 's',   'т' => 't',   'у' => 'u',
    'ф' => 'f',   'х' => 'h',   'ц' => 'c',
    'ч' => 'ch',  'ш' => 'sh',  'щ' => 'sch',
    'ь' => '\'',  'ы' => 'y',   'ъ' => '\'',
    'э' => 'e',   'ю' => 'yu',  'я' => 'ya'
];

// Функция транслитерации строки
function transliterate($string, $alfabet) {
    // Приводим строку к нижнему регистру для сопоставления с ключами массива
    $string = mb_strtolower($string);

    // Переменная для хранения результата
    $transliteratedString = '';

    // Проходим по каждому символу строки
    for ($i = 0; $i < mb_strlen($string); $i++) {
        // Получаем текущий символ
        $char = mb_substr($string, $i, 1);

        // Если символ есть в массиве, заменяем его на соответствующее значение
        if (array_key_exists($char, $alfabet)) {
            $transliteratedString .= $alfabet[$char];
        } else {
            // Если символ не найден в массиве (например, пробел или знак), просто добавляем его как есть
            $transliteratedString .= $char;
        }
    }

    // Возвращаем результат
    return $transliteratedString;
}

// Пример использования функции
echo transliterate('ПрИвет, кАк дЕЛа?', $alfabet) . PHP_EOL;  // privet, kak dela?
echo transliterate('МосквА', $alfabet) . PHP_EOL;  // moskva
echo transliterate('Тестирование транслитерации', $alfabet) . PHP_EOL;  // testirovanie transliteracii
echo transliterate('Привет, мир!', $alfabet) . PHP_EOL;  // privet, mir!

?>

