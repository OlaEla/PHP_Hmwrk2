<!--использоваться названия областей, а в качестве значений – -->
<!--массивы с названиями городов из соответствующей области. -->
<!--Вывести в цикле значения массива, чтобы результат был таким: -->
<!--Московская область: Москва, Зеленоград, Клин -->
<!--Ленинградская область: Санкт-Петербург, Всеволожск, Павловск, Кронштадт -->
<!--Рязанская область … (названия городов можно найти на maps.yandex.ru).-->

<?php
// Массив с областями и городами
$regions = [
    "Московская область" => ["Москва", "Зеленоград", "Клин"],
    "Ленинградская область" => ["Санкт-Петербург", "Всеволожск", "Павловск", "Кронштадт"],
    "Рязанская область" => ["Рязань", "Касимов", "Скопин"]
];

// Цикл для вывода областей и городов
foreach ($regions as $region => $cities) {
    // Выводим область
    echo "$region: ";
    // Выводим города, объединяя их через запятую
    echo implode(", ", $cities);
    echo "\n"; // Перенос строки после вывода городов
}
?>

