<!--С помощью рекурсии организовать функцию возведения числа в степень.
Формат: function power($val, $pow), где $val – заданное число, $pow – степень.-->

<?php
// Функция для возведения числа $val в степень $pow с помощью рекурсии
function power($val, $pow) {
    // Базовый случай: любое число в степени 0 равно 1
    if ($pow == 0) {
        return 1;
    }

    // Если степень отрицательная, используем обратное значение
    if ($pow < 0) {
        return 1 / power($val, -$pow);
    }

    // Рекурсивный случай: продолжаем умножать число на результат с уменьшенной степенью
    return $val * power($val, $pow - 1);
}

// Примеры использования функции
echo power(2, 3) . PHP_EOL;  // 2^3 = 8
echo power(5, 0) . PHP_EOL;  // 5^0 = 1
echo power(2, -2) . PHP_EOL; // 2^-2 = 0.25
echo power(3, 4) . PHP_EOL;  // 3^4 = 81
?>

