<!--Реализовать основные 4 арифметические операции в виде функции с -->
<!--тремя параметрами – два параметра это числа, третий – операция. -->
<!--Обязательно использовать оператор return.-->

<?php
// Функция для выполнения арифметических операций
function calculate($num1, $num2, $operation) {
    // Проверяем типы данных, чтобы убедиться, что переданы числа
    if (!is_numeric($num1) || !is_numeric($num2)) {
        return "Ошибка: оба параметра должны быть числами.";
    }

    // Используем оператор switch для выбора нужной операции
    switch($operation) {
        case '+':
            // Операция сложения
            return $num1 + $num2;
        case '-':
            // Операция вычитания
            return $num1 - $num2;
        case '*':
            // Операция умножения
            return $num1 * $num2;
        case '/':
            // Проверяем, чтобы не делить на ноль
            if ($num2 == 0) {
                return "Ошибка: деление на ноль невозможно.";
            }
            return $num1 / $num2;
        default:
            // Если передана некорректная операция, возвращаем сообщение об ошибке
            return "Ошибка: неизвестная операция '$operation'. Используйте +, -, * или /.";
    }
}

// Пример использования функции:
echo calculate(10, 5, '+');  // 15
echo "\n";
echo calculate(10, 5, '-');  // 5
echo "\n";
echo calculate(10, 5, '*');  // 50
echo "\n";
echo calculate(10, 0, '/');  // Ошибка: деление на ноль невозможно.
echo "\n";
echo calculate(10, 5, '/');  // 2
echo "\n";
echo calculate(10, 5, '^');  // Ошибка: неизвестная операция '^'.
?>

